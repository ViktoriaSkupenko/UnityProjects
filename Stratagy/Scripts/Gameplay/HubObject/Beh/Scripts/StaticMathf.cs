namespace Gameplay.HubObject.Beh.Scripts
{
    public static class StaticMathf
    {
        public static float Add(float t, float t2) => t + t2;
        public static float Mul(float t, float t2) => t * t2;
        public static float Div(float t, float t2) => t / t2;
        public static float Minus(float t, float t2) => t - t2;
    }
}